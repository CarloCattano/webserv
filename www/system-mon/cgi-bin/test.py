#!/usr/bin/env python3
import time
import os

# for loop that counts to 10
print(f"<!DOCTYPE html>")
print(f"<html>")
print(f"<head>")
print(f"<title>Counting to 10</title>")
print(f"</head>")
print(f"<body>")

for i in range(30):
    print(f"<hr>")
    print(f"<p>{i}</p>")
    print(f"<hr>")
    time.sleep(0.2)

print(f"</body>")
print(f"</html>")

